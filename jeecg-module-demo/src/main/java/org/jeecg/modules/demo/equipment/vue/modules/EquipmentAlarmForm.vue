            </a-form-model-item>
          </a-col>
          <a-col :span="24">
            <a-form-model-item label="设备类型" :labelCol="labelCol" :wrapperCol="wrapperCol" prop="deviceType">
              <a-input v-model="model.deviceType" placeholder="请输入设备类型"  ></a-input>
            </a-form-model-item>
          </a-col>
          <a-col :span="24">
            <a-form-model-item label="设备名称" :labelCol="labelCol" :wrapperCol="wrapperCol" prop="deviceName">
              <a-input v-model="model.deviceName" placeholder="请输入设备名称"  ></a-input>
            </a-form-model-item>
          </a-col>
          <a-col :span="24">
            <a-form-model-item label="报警开始时间" :labelCol="labelCol" :wrapperCol="wrapperCol" prop="startDatetime">
              <j-date placeholder="请选择报警开始时间"  v-model="model.startDatetime" :show-time="true" date-format="YYYY-MM-DD HH:mm:ss" style="width: 100%" />
            </a-form-model-item>
          </a-col>
          <a-col :span="24">
            <a-form-model-item label="报警名称" :labelCol="labelCol" :wrapperCol="wrapperCol" prop="alarmInfo">
              <a-input v-model="model.alarmInfo" placeholder="请输入报警名称"  ></a-input>
            </a-form-model-item>
          </a-col>
          <a-col :span="24">
            <a-form-model-item label="报警等级" :labelCol="labelCol" :wrapperCol="wrapperCol" prop="alarmType">
              <a-input v-model="model.alarmType" placeholder="请输入报警等级"  ></a-input>
            </a-form-model-item>
          </a-col>
          <a-col :span="24">
            <a-form-model-item label="报警" :labelCol="labelCol" :wrapperCol="wrapperCol" prop="alarmReason">
              <a-input v-model="model.alarmReason" placeholder="请输入报警"  ></a-input>
            </a-form-model-item>
          </a-col>
          <a-col :span="24">
            <a-form-model-item label="解决方法" :labelCol="labelCol" :wrapperCol="wrapperCol" prop="solvingMethod">
